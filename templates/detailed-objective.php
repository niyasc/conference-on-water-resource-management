<p>
	The concept of Water Resource Management (WRM) has gained wide acceptance as a successful way to tackle
these challenges. The aim is to promote economic and social development by addressing and balancing diverse water use interests, while simultaneously safeguarding water resources. However, current water management practices are far from fulfilling these objectives for different reasons. In some cases,adequate management structures and techniques are not available while in other cases existing management approaches fail in properly addressing the key problems.
